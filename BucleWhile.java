
public class BucleWhile {

    public static void main(String args[]) {
      int contador = 1;
      while(contador <= 5){
          System.out.println("Contador: " + contador); 
          contador++;
     }
      
      //Bucle do-while
      int numero = 1;
      do{
          System.out.println("Numero: " + numero);
          numero++;
      }while(numero >= 5);
    }
}
